# Iterate over each URL
for url in urls:
    try:
        # Fetch the HTML content of the page
        response = requests.get(url)
        response.raise_for_status()  # Ensure we got a successful response

        # Parse the HTML content
        tree = html.fromstring(response.content)

        # Extract data using the provided XPath expressions
        row = [url]  # Start with the URL in the first column
        for xpath in xpaths:
            elements = tree.xpath(xpath)
            if elements:
                # Append the text content of the found element
                row.append(elements[0].text_content().strip())
            else:
                # Append an empty string if the element was not found
                row.append("")

        # Add the row to results
        results.append(row)

    except requests.RequestException as e:
        print(f"Error fetching {url}: {e}")
        # Add a row with the URL and errors if the request fails
        results.append([url] + ["Error"] * len(xpaths))

# Define the header for the CSV file
header = ['URL'] + [f'XPath {i+1}' for i in range(len(xpaths))]

# Write the results to a CSV file
with open('scraped_data.csv', 'w', newline='', encoding='utf-8') as csvfile:
    writer = csv.writer(csvfile)
    writer.writerow(header)  # Write the header
    writer.writerows(results)  # Write the data rows

